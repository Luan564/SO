//operaciones.c


int suma (int a, int b){
    return a+b;
}
int resta (int a, int b){
    return a-b;
}
int multiplicacion (int a, int b){
    return a*b;
}
float division (float a, float b){
    return a/b;
}


int pasoPorValor(int a){
    return a = 10;
}
int pasoPorReferencia(int *a){
    return (*a) = -10;
}
